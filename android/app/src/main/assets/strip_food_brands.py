def main():
    with open(FILE, "r", encoding="utf-8") as f:
        lines = f.readlines()
    out = []
    removed = []
    for line in lines:
        line = line.rstrip("\n\r")
        if not line.strip():
            out.append(line)
            continue
        parts = line.split(None, 1)
        if len(parts) != 2:
            out.append(line)
            continue
        py, w = parts[0], parts[1]
        if w in REMOVE_WORDS:
            removed.append((py, w))
            continue
        out.append(line)
    with open(FILE, "w", encoding="utf-8", newline="\n") as f:
        f.write("\n".join(out))
        if out:
            f.write("\n")
    print("已剔除 %d 条: %s" % (len(removed), removed))


if __name__ == "__main__":
    main()
